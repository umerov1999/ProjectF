int crc32(const std::string &str) {
    unsigned int uiCRC32 = 0xFFFFFFFF;
    auto *pszData = (unsigned char *) str.c_str();

    for (size_t i = 0; i < str.length(); ++i)
        uiCRC32 = ((uiCRC32 >> 8) & 0x00FFFFFF) ^
                  uiCRC32_Table[(uiCRC32 ^ (unsigned int) *pszData++) & 0xFF];

    return (int) (uiCRC32 ^ 0xFFFFFFFF);
}

static void
base36_encode(std::ostringstream &s, uint64_t id) {
    char digit;
    while (id != 0) {
        digit = char(id % 36);
        if (digit < 10)
            s.put('0' + digit);
        else
            s.put('a' + (digit - 10));
        id /= 36;
    }
    if (s.str().empty()) {
        s.put('0');
    }
}

std::string dumpCrc64(uint64_t crc) {
    std::ostringstream v;
    base36_encode(v, crc);
    return v.str();
}

void crc64(uint64_t &crc, const void *s, int l) {
    for (int j = 0; j < l; j++) {
        uint8_t byte = ((const char *) s)[j];
        crc = crc64_tab[(uint8_t) crc ^ byte] ^ (crc >> 8);
    }
}